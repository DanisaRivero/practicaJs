/*
* ESTRUCTURAS ITERATIVAS 
! ==> WHILE
! ==> DO-WHILE
! ==> FOR
*/
/*
? WHILE ==> Mientras
* Este ciclo se utiliza cuando
! no sabemos
* el número de veces exacto que se ejecutará en el ciclo.
*/
let iteracion = 0;
while (iteracion < 5){ //! Mientras iteracion sea menor que 5
  alert('Hola mundo'); //! Ejecuta el código
  iteracion++;  //! Salimos del bucle hasta que 0 sea mayor que 5
}
 alert('El ciclo terminó');